# Basic Tuple Membership operation in and not in
Tuple1 = ("Delowar",602826,"Motu",58975)
tor = ("Joty",89709,"Majdur",90832)

# Uses of  Membership operator in
print("Fahim" in Tuple1)
print("Delowar" in Tuple1)
print("Majdur" in tor)
print(44489709 in tor)

# Uses of Membership operator not in
print("Delowar" not in Tuple1)
print("Delowar" not in tor)
