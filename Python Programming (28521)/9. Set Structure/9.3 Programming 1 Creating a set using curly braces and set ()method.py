# Creating a set using curly braces and set ()method
s = set("    ")
print(type(s))
print(s)

Str_set = {'K','a','d','i','r'}
print(type(Str_set))
print(Str_set)

num_set = {4,0,1,2,3}
print(3 in num_set)
print(num_set)

word_set = set(["Kadir","Wahid","Meher","Sohel"])
print(sorted(word_set))
print("Kadir" not in word_set)
print("Wahid" in word_set)