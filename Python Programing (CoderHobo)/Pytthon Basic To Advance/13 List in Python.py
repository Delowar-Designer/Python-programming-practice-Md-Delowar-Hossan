# List in Python
my_list = [1,2,3,"Mitu",4,5,6]
print(my_list[3])
print(my_list[-2])
print(type(my_list))
print(len(my_list))

# Listt Slicig in Python
my_list1 = [1,2,3,4,5,6,7,8]
print(my_list1[2:4])
print(my_list1[1:6:2])
print(my_list1[-4:-2])

