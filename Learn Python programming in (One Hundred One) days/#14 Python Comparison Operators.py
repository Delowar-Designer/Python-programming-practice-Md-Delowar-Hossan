# Python Comparison Operators
print('Python Comparison Operators')
# Comparison operators are used to compare two values:

# Operator	    Name	                    Example	Try it
# ==	        Equal	                    x == y
x = 5
y = 10
print(x == y)
# !=	        Not equal	                x != y
print(x != y)
# >	            Greater than	            x > y
print(x > y)
# <	            Less than	                x < y

print(x < y)
# >=	        Greater than or equal to	x >= y
print(x >= y)
# <=	        Less than or equal to	    x <= y
print(x <= y)