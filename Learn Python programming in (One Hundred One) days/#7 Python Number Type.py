#7 Python Number Type
'''
Numeric Types:	int, float, complex
x = 20	int
x = 20.5	float
x = 1j	complex

x = int(20)	int
x = float(20.5)	float
x = complex(1j)	complex
'''
# int
num1 = 455557522335

print(num1)
print(type(num1))

# float
Gpa = 4254545454.2777777

print(Gpa)
print(type(Gpa))

# complex
rom = 2j

print(rom)
print(type(rom))

