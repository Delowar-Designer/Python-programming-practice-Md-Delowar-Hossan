# Python Type Casting
# int
name = "122222"
print(int(name))
print(type(int(name)))

x = int(1)
y = int(2.22)
z = int("32")
print(x)
print(type(int(x)))
print(y)
print(type(y))
print(z)
print(type(z))



# Type Casting float
